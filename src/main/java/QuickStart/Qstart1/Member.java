package QuickStart.Qstart1;
import java.util.* ;

public class Member {
    int id ;
    String name ;
    int     roomNum ;

    public void setId ( int id )
    {
        this.id = id ;
    }

    public void setName ( String name )
    {
        this.name = name ;
    }

    public void setRoomNum( int roomNum )
    {
        this.roomNum = roomNum ;
    }

    public int getId()
    {
        return id ;
    }
    public String getName()
    {
        return name ;
    }

    public int getRoomNum()
    {
        return roomNum ;
    }

}
